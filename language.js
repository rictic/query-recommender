function parseLanguage(code) {
  var match = code.match(/^\s*(.*?)(-(.*))?\s*$/);
  if (!match) { return code; }
  return {
    language: ISO_639_1_MAP[match[1].toLowerCase()],
    country: ISO_3166_1_MAP[match[3] ? match[3].toUpperCase() : null]
  };
}

